DROP SCHEMA IF EXISTS kdrama_schema;
CREATE SCHEMA kdrama_schema;
USE kdrama_schema;

DROP TABLE IF EXISTS Shows;
CREATE TABLE Shows (
    ShowID INT NOT NULL AUTO_INCREMENT,
    Name VARCHAR(255) NOT NULL,
    NumOfEpisodes INT NOT NULL,
    Duration VARCHAR(255) NOT NULL,
    ContentRating VARCHAR(255) DEFAULT NULL,
    Rating FLOAT(2, 1) NOT NULL,
    PRIMARY KEY (ShowID)
);

DROP TABLE IF EXISTS Crew;
CREATE TABLE Crew (
	CrewID INT NOT NULL AUTO_INCREMENT,
    Name VARCHAR(255) NOT NULL,
    Title VARCHAR(255) NOT NULL,
    PRIMARY KEY (CrewID)
);

DROP TABLE IF EXISTS Airing;
CREATE TABLE Airing (
	ShowID INT NOT NULL AUTO_INCREMENT,
    AirDateStart VARCHAR(255) NOT NULL,
    YearOfRelease YEAR NOT NULL,
    OriginalNetwork VARCHAR(255) NOT NULL,
    AiredOn VARCHAR(255) NOT NULL,
    AirDateEnd VARCHAR(255),
    PRIMARY KEY (ShowID)
);

DROP TABLE IF EXISTS Genre;
CREATE TABLE Genre (
	GenreID INT NOT NULL AUTO_INCREMENT,
    GenreName VARCHAR(255) NOT NULL,
	PRIMARY KEY(GenreID)
);

DROP TABLE IF EXISTS ProductionCompany;
CREATE TABLE ProductionCompany (
	CompanyID INT NOT NULL AUTO_INCREMENT,
    Name VARCHAR(255) NOT NULL,
    PRIMARY KEY (CompanyID)
);

DROP TABLE IF EXISTS CrewToShow;
CREATE TABLE CrewToShow (
	ShowID INT NOT NULL,
    CrewID INT NOT NULL,
    FOREIGN KEY (ShowID) REFERENCES Shows(ShowID),
    FOREIGN KEY (CrewID) REFERENCES Crew(CrewID)
);

DROP TABLE IF EXISTS GenreToShow;
CREATE TABLE GenreToShow (
	ShowID INT NOT NULL,
    GenreID INT NOT NULL,
    FOREIGN KEY (ShowID) REFERENCES Shows(ShowID),
    FOREIGN KEY (GenreID) REFERENCES Genre(GenreID)
);

DROP TABLE IF EXISTS ProductionToShow;
CREATE TABLE ProductionToShow (
	ShowID INT NOT NULL,
    CompanyID INT NOT NULL,
    FOREIGN KEY (ShowID) REFERENCES Shows(ShowID),
    FOREIGN KEY (CompanyID) REFERENCES ProductionCompany(CompanyID)
);
